package OperadoresRelacionais;

public class exemplo3 {

	public static void main(String[] args) {

		int n1 = 20;
		int n2 = 50;

		if (n1 == n2) {
			System.out.println("As notas são iguais " + n1 + "  " + n2);

		} else if (n1 != n2) {
			System.out.println("As notas são Diferentes " + n1 + "  " + n2);
		}
	}

}
