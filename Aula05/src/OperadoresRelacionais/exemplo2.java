package OperadoresRelacionais;

public class exemplo2 {

	public static void main(String[] args) {

		int n1 = 600;
		int n2 = 30;

		if (n1 <= n2) {
			System.out.println(n1 + " é menor ou igual que " + n2);
		} else if (n1 >= n2) {
			System.out.println(n1 + " é maior ou igual que " + n2);
		}

	}
}
